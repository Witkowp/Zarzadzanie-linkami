    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String adr, name, direct;
        adr=jTextField1.getText();
        name=jTextField2.getText();
        if(adr.length()==0){
            return;
        }
        if(name.length()==0){
            try{
                name=TitleExtractor.getPageTitle(adr);
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Can't get page title.", "", JOptionPane.ERROR_MESSAGE);
            }
        }
        direct=dir;
        if(links!=null){
        if(name.length()!=0&&validateHTTP_URI(adr)){
            try{
                Data newLink=new Data(BazaDanych.insertIntoSQL(adr, name, direct),name,adr,dir);
                links.add(newLink);
            }catch(Exception e){
                jTextField1.setText("");
                JOptionPane.showMessageDialog(null, "SQL Add Link Error.", "", JOptionPane.ERROR_MESSAGE);
                return;
            }
            addlinkButtons();
            jTextField1.setText("");
            jTextField2.setText("");
            return;
        }
        jTextField1.setText("");
        JOptionPane.showMessageDialog(null, "Wrong adress.", "", JOptionPane.ERROR_MESSAGE);
        return;
        }
        jTextField1.setText("");
        JOptionPane.showMessageDialog(null, "DB not downloaded.", "", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

