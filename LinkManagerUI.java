    private void addlinkButtons(){
        int y=15, count=0;
        int h=451;
        jPanel2.setPreferredSize(new Dimension(350,h));
        jPanel2.removeAll();
        for(int i=0;i<links.size();i++){
            if(links.get(i).direct.equals(dir)&&links.get(i).name.equals("")==false&&links.get(i).address.equals("")==false){
                count++;
                if(count>17){
                    h=h+25;
                    jPanel2.setPreferredSize(new Dimension(350,h));
                    jPanel2.setSize(new Dimension(350,h));
                }
                MyjButton b=new MyjButton(links.get(i).name);
                b.setBounds(15, y, 300, 20);
                b.setOpaque(false);
                b.setFocusPainted(false);
                b.setBorderPainted(false);
                b.setContentAreaFilled(false);
                try {
                    Image img1 = ImageIO.read(getClass().getResource("/my/linkbutt.png"));
                    Image img2 = ImageIO.read(getClass().getResource("/my/linkbuttmouse.png"));
                    Image img3 = ImageIO.read(getClass().getResource("/my/linkbuttpress.png"));
                    b.setIcon(new ImageIcon(img1));
                    b.setRolloverIcon(new ImageIcon(img2));
                    b.setPressedIcon(new ImageIcon(img3));
                  } catch (Exception ex) {
                      JOptionPane.showMessageDialog(null, "icon", "", JOptionPane.ERROR_MESSAGE);
                  }
                b.setHorizontalTextPosition(JButton.CENTER);
                b.setVerticalTextPosition(JButton.CENTER);
                b.adress=links.get(i).address;
                b.id=links.get(i).id;
                b.addActionListener(new java.awt.event.ActionListener() {
                    @Override
                    public void actionPerformed(java.awt.event.ActionEvent evt) {
                        if(delt==0){
                            try{
                                URI adr=new URI(b.adress);
                                openWebpage(adr);
                            }catch(java.net.URISyntaxException e){
                            }
                        }else if(delt==1){
                            for(int j=0;j<links.size();j++){
                                if(links.get(j).id==b.id){
                                    try{
                                        BazaDanych.deleteFromSQL(b.id);
                                        links.remove(j);
                                        j--;
                                    }catch(Exception e){
                                        JOptionPane.showMessageDialog(null, "SQL Link Delete Error.", "", JOptionPane.ERROR_MESSAGE);
                                    }
                                }
                            }
                            addlinkButtons();
                        }

                    }
                });
                jPanel2.add(b,BorderLayout.CENTER);
                y=y+25;
            }    
        }
        jPanel2.repaint();
        
    }
    
